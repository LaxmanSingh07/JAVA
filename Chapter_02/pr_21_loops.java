public class pr_21_loops {
    public static void main(String[] args) {

        int i = 0;
        while (i < 3) {
            System.out.println(i);
            i++;
        }

        System.out.println("Done bro");

        while(true)
        {
            System.out.println("I am in a infinite while loop");
        }
    }
}
